func (h *ClientController) RevokeTariff(c *gin.Context) {
	clientID, err := strconv.Atoi(c.Param("id"))
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "invalid id"})
		return
	}
	tariffRecordID, err := strconv.Atoi(c.Param("tariff_record_id"))
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "invalid tariff_record_id"})
		return
	}
	if err := h.clientSvc.RevokeTariff(c.Request.Context(), clientID, tariffRecordID); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}
	c.JSON(http.StatusOK, gin.H{"message": "tariff revoked"})
}

